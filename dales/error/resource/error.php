<?php namespace dales\error;

class Error
{
    /**
     * @param null $message
     * @param null $error
     */
///////////////////////////////////////////////////////////////////////////////
//    Simple Error Displays
///////////////////////////////////////////////////////////////////////////////

    public static function send($message = null, $error = null)
    {
        echo $message;
        echo '<br>'.$error;
    }

///////////////////////////////////////////////////////////////////////////////
//    None Maskable Error Pages
///////////////////////////////////////////////////////////////////////////////

    public static function page($error = null)
    {
        echo '<br>'.$error;
        exit;
    }

}
